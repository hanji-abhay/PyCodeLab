a = '''
i am abhay.
i want to be an ai/ml developer
'''
c =  a.capitalize()   #capitalizes the string
print(c)
b = a.title()         #converts the string in (Title Format)
print(b)
u = a.upper()         #converts string in upper case
d = a.isupper()       #returns true or false when string is in upper case or not
print(d)
l = u.lower()         #converts string in lower case
d = l.islower()       #returns true or false when string is in lower case or not
print(d)
