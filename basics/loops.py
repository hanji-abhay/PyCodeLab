# Loops in Python
# For loop example
print("For loop: 1 to 5")  #First message displayed on the output screen
for i in range(1, 6):      #for loop set between the range from 1 to 6 
    print(i, end=" ")      #prints the output
print()                    #print

# While loop example
print("While loop: 5 to 1") #First message displayed on the output screen
count = 5                   #variable count is declared and initialized with value 5
while count > 0:            #condition applied
    print(count, end=" ")   #prints the output
    count -= 1              #increment or decrement 
print()                     #print
