# Variables in Python
# A variable is use to store values in it
age = 20    #It is a variable named age, which hold a data of integer datatype.
height = 5.9   # It hold float type data in it.
is_student = True  # Boolean type datatype variable.
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# To find the datatype of the variable just add type() method in print statement
print(type(age))
print(type(height))
print(type(is_student))
